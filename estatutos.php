        <h4 style="font-weight: bold;">CAPÍTULO I. DE LA DENOMINACIÓN, FINES, ACTIVIDADES, DOMICILIO Y
            ÁMBITO DE LA ASOCIACIÓN</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>
                Artículo 1. Denominación
            </h5>
            <ol>
                <li> Por los presentes Estatutos, se constituye por tiempo indefinido la Asociación
                    <span style="font-style:italic">“Artificial Intelligence Student Collective”</span> de la
                    Universidad Carlos III de Madrid.
                </li>
                <li>A los efectos de estos Estatutos y de la actividad universitaria común, las siglas de la
                    Asociación serán “AISC”. </li>
                <li>AISC está sometida a la legislación vigente en materia de Asociaciones asi como a la
                    normativa universitaria aplicable y al resto del ordenamiento jurídico. </li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 2. Fines y actividades</h5>
            <ol>
                <li>AISC Madrid tiene como fines promover el interés y la formación en inteligencia
                    artificial y áreas relacionadas entre los estudiantes de la UC3M, impulsar el talento
                    universitario mediante experiencias formativas y colaborativas, favorecer la proyección
                    profesional de sus miembros a través del contacto con la industria tecnológica y
                    fomentar un espacio de innovación y cooperación dentro y fuera de la universidad.
                </li>
                <li>Con vistas a conseguir este fin, las actividades de AISC serán:
                    <ol style="list-style-type:lower-alpha">
                        <li>Facilitar recursos y medios de formación relacionados con la ingeniería en general y
                            la Inteligencia Artificial en particular.</li>
                        <li>Fomentar y dar valor al talento y el compromiso universitario a través de talleres
                            prácticos, charlas profesionales y actividades comprometidas a la innovación. </li>
                        <li> Impulsar a los alumnos hacia el mundo profesional facilitando oportunidades en el
                            mismo mediante la conexión entre la industria tecnológica y los miembros de la
                            asociación. </li>
                        <li> Promover la colaboración con otras asociaciones universitarias y la conexión entre
                            alumnos de diferentes disciplinas a través de proyectos cooperativos.</li>
                        <li> Reforzar y mantener la conexión de los alumnos con el resto de ramas
                            internacionales de AISC.</li>
                    </ol>
                </li>
            </ol>
        </section>
        <section class="mb-4" style="color:black;">
            <h5>Artículo 3. Domicilio social y ámbito de actuación</h5>
            <ol>
                <li>El domicilio social de AISC se establece en Avenida de la Universidad, N.º 30,
                    Campus de la Escuela Politécnica Superior de la Universidad Carlos III de Madrid,
                    despacho 2.3.C05, localidad de Leganés, código postal 28911.
                </li>
                <li>El ámbito de actuación principal de la Asociación será la Comunidad de Madrid, sin
                    perjuicio de poder realizar actividades y colaboraciones con entidades públicas o
                    privadas en el resto del territorio nacional e internacional.
                </li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 4. Tipos de socios</h5>
            <ol>
                <li>Se establecen dos tipos de socios dentro de AISC; Socios activos y socios de honor.</li>
                <li>Podrán ser socios activos de AISC todos los alumnos matriculados en la Universidad
                    Carlos III de Madrid, independientemente de sus estudios y con derecho a asociarse
                    según la normativa vigente y cuando asi lo soliciten libremente.</li>
                <li>Por otro lado, existirá la figura de Socio de Honor para aquellos que ya no forman
                    parte como socios activos de AISC y que por su prestigio o por haber contribuido de
                    modo relevante a los fines de la Asociación, se hagan acreedores de tal distinción.</li>
            </ol>
        </section>

        <br>
        <h4 style="font-weight:bold;">CAPITULO II. DE LOS ÓRGANOS DE GOBIERNO DE LA ASOCIACIÓN</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 5. Órganos de gobierno.</h5>
            <p>La asociación se regirá por los siguientes órganos:</p>
            <ol style="list-style-type:lower-alpha">
                <li>La Asamblea General, tanto en su forma Ordinaria como Extraordinaria.</li>
                <li>La Junta Directiva.</li>
            </ol>
        </section>
        <br>
        <h4 style="font-weight:bold;">CAPÍTULO III. DE LA ASAMBLEA GENERAL</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 6. Integración de la Asamblea General.</h5>
            <p>La Asamblea General estará conformada por todos los socios, siendo el órgano
                supremo de la Asociación y conformando su voluntad mediante el voto de sus
                miembros.</p>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 7. Quórum de las Asambleas.</h5>
            <ol>
                <li>La Asamblea General, sea Ordinaria o Extraordinaria, se dará por constituida
                    válidamente cuando concurran en ella, presentes o telemáticamente si así se avisó
                    previamente a la Junta Directiva, un número de asociados activos igual o mayor a 1/2
                    del total de los mismos.</li>
                <li>Para la aprobación de acuerdos en la Asamblea General Ordinaria y en la
                    Extraordinaria, se requerirá una mayoría simple de los socios activos asistentes a la
                    Asamblea, es decir, más votos a favor que en contra.</li>
                <li>No obstante, en la Asamblea General Extraordinaria y cuando se refiera a decisiones
                    relativas al artículo 11.1 letras a), b) o c), se requerirá de mayoría absoluta de los
                    socios activos asistentes para su aprobación. Es decir, que los votos a favor superen a
                    la mitad del total de los votos.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 8. Convocatoria de la Asamblea General Ordinaria</h5>
            <ol>
                <li>La Asamblea General Ordinaria se reunirá, como mínimo, una vez cada cuatrimestre
                    académico.</li>
                <li>Se entenderá que el curso académico se divide en dos periodos cuatrimestrales,
                    según el calendario oficial de la Universidad Carlos III de Madrid.</li>
                <li>La Junta Directiva tratará, en la medida de lo posible, de elegir el día más
                    conveniente para la celebración de la Asamblea General Ordinaria, de tal manera que
                    puedan asistir el mayor número de socios.</li>
                <li>La convocatoria de la Asamblea General Ordinaria irá acompañada de su respectivo
                    orden del día y la misma debe ser anunciada con mínimo catorce días naturales de
                    antelación por escrito a cada asociado.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 9. Competencias de la Asamblea General Ordinaria</h5>
            <p>Serán competencias de la Asamblea General Ordinaria:</p>
            <ol style="list-style-type:lower-alpha">
                <li>La elección, revocación y renovación de los miembros de la Junta Directiva</li>
                <li>El examen y la aprobación de las cuentas, presupuestos y aplicación de los fondos
                    de la Asociación.</li>
                <li>La aprobación de cuotas</li>
                <li>El nombramiento de socios honorarios a propuesta de la Junta Directiva</li>
                <li>La aprobación del Reglamento de Régimen Interno</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 10. Convocatoria de la Asamblea General Extraordinaria</h5>
            <ol>
                <li>La Asamblea General Extraordinaria se reunirá cuando lo encuentre necesario la
                    Junta Directiva y será convocada por el Presidente con su correspondiente orden del
                    día y con un mínimo de siete días naturales de antelación.</li>
                <li>El plazo indicado en el apartado anterior de este Artículo no será de aplicación
                    cuando se trate de una Asamblea General Extraordinaria urgente.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 11. Competencias de la Asamblea General Extraordinaria</h5>
            <ol>
                <li>Serán competencias de la Asamblea General Extraordinaria:
                    <ol style="list-style-type:lower-alpha">
                        <li>La disolución de la Asociación.</li>
                        <li>La modificación de los Estatutos.</li>
                        <li>La autorización para la enajenación de bienes propiedad de la asociación.</li>
                        <li>La elección de los miembros de la Junta Directiva en el caso de cesar alguno de ellos
                            en su cargo antes de la correspondiente Asamblea General Ordinaria.</li>
                        <li>La expulsión de cualquier socio a propuesta de la Junta Directiva.</li>
                    </ol>
                </li>
                <li>En el caso de la letra d) del apartado anterior, la Asamblea General Extraordinaria
                    tendrá carácter urgente.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 12. Asamblea General Extraordinaria Urgente</h5>
            <ol>
                <li>La Asamblea General Extraordinaria Urgente podrá convocarse en situaciones
                    excepcionales e inaplazables que requieran decisiones inmediatas para el
                    funcionamiento de la Asociación, como cobertura de vacantes, aceptación de
                    subvenciones con plazos cortos o respuesta a requerimientos oficiales.</li>
                <li>La convocatoria corresponde al Presidente, a iniciativa propia, de la Junta Directiva o
                    a solicitud de, al menos, un tercio de los socios activos.</li>
                <li>La citación deberá realizarse con un mínimo de 48 horas de antelación por medios
                    electrónicos que aseguren la recepción de la comunicación. Debe incluir: fecha, hora y
                    lugar; orden del día; y justificación de la urgencia.</li>
                <li>La Asamblea quedará válidamente constituida con al menos un tercio de los socios
                    activos, siempre que concurran mínimo el Presidente, el Secretario y un socio
                    adicional. Solo se adoptarán acuerdos sobre asuntos cuya urgencia esté justificada.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 13. Acuerdos alcanzados por las Asambleas</h5>
            <ol>
                <li>La Asamblea General dejará constancia de los acuerdos que adopte e incluirá las
                    firmas del presidente y del secretario de la Asociación. Los acuerdos serán publicados
                    en un apartado específico dentro de la web de la asociación y serán de fácil acceso.</li>
                <li>El acta de la Asamblea General en la que se elige una nueva Junta Directiva será
                    firmada por los miembros de la Junta saliente.</li>
            </ol>
        </section>




        <br>
        <h4 style="font-weight:bold;">CAPÍTULO IV. DE LA JUNTA DIRECTIVA</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 14. Naturaleza y composición.</h5>
            <ol>
                <li>La Junta Directiva es el órgano que gestiona y representa los intereses de la Asociación.</li>
                <li>La Junta Directiva estará compuesta como mínimo por el Presidente, el Secretario General, el
                    Vicepresidente, el Tesorero y uno o varios Vocales, según se determine por la Asamblea General.</li>
                <li>Todos los miembros de la Junta Directiva serán mayores de edad y deben tener la capacidad de obrar y
                    ostentar la condición de socio activo de AISC Madrid.</li>
                <li>Por razones organizativas, los cargos de Vicepresidente y Secretario General podrán ser desempeñados
                    por la misma persona, sin que ello afecte a sus competencias ni a la validez de los actos de la
                    Junta Directiva.</li>
                <li>Los Vocales forman parte de la Junta Directiva y desempeñarán las funciones que la misma les asigne,
                    apoyando las áreas de actividad de la Asociación y colaborando con el Presidente y demás miembros en
                    la consecución de los fines de la Asociación.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 15. Competencias.</h5>
            <ol>
                <li>La Junta Directiva tendrá las siguientes competencias:
                    <ol style="list-style-type:lower-alpha">
                        <li>Coordinar el funcionamiento de los distintos órganos de la Asociación.</li>
                        <li>Ejecutar los acuerdos adoptados por la Asamblea General.</li>
                        <li>Proponer a la Asamblea General la expulsión de socios por motivos razonados.</li>
                        <li>Preparar y presentar, en su caso, las cuentas de la Asociación ante la Asamblea para su
                            eventual aprobación anual.</li>
                        <li>Velar por el cumplimiento de los Estatutos.</li>
                    </ol>
                </li>
                <li>Asimismo, se encargará de realizar una memoria anual que recopile todas las actividades, eventos y
                    actos durante el curso académico. El Presidente será el responsable de publicarla y presentarla en
                    la Asamblea General Ordinaria.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 16. Elección de miembros.</h5>
            <ol>
                <li>La elección de los miembros de la Junta Directiva por la Asamblea General Ordinaria se realizará
                    mediante la presentación de candidaturas. El Presidente convocará dicha presentación con al menos
                    veinte días naturales de antelación a la celebración de la Asamblea General Ordinaria. A cada
                    candidatura se le garantizará una difusión adecuada e igualitaria. El anterior plazo no será de
                    aplicación para cuando se elijan los miembros de la Junta Directiva por medio de Asamblea General
                    Extraordinaria Urgente.</li>
                <li>Los requisitos de elegibilidad y presentación de candidatura son los expresados en el artículo 14.3.
                </li>
                <li>Cada candidatura se presentará de forma conjunta para todos los cargos de la Junta Directiva. La
                    elección del Presidente supondrá automáticamente la del resto de los miembros de su misma
                    candidatura. Se admitirá, no obstante, candidatura individual en el caso de haber cesado en su cargo
                    alguno de los miembros de la Junta Directiva.</li>
                <li>Resultará electa aquella candidatura que obtenga el mayor número de votos entre los socios activos
                    presentes.</li>
                <li>La elección ordinaria de la Junta Directiva se realizará en la Asamblea General Ordinaria del
                    segundo cuatrimestre, prevista normalmente entre los meses de abril y junio.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 17. Duración del mandato y reelección.</h5>
            <ol>
                <li>La duración del mandato de los miembros electos de la Junta Directiva será de un año desde la fecha
                    de su nombramiento.</li>
                <li>Los miembros de la Junta Directiva podrán optar a la reelección en cualquier cargo.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 18. Reuniones y quórum.</h5>
            <p>La Junta Directiva se reunirá previa convocatoria debiendo mediar al menos 3 días entre ésta y su
                celebración, cuantas veces lo determine su Presidente y a petición de 2/5 de sus miembros. Quedará
                constituida cuando asista la mitad más uno de sus miembros y para que sus acuerdos sean válidos deberán
                ser adoptados por mayoría simple de votos. En caso de empate, será de calidad del voto del Presidente o
                de quien haga sus veces.</p>
        </section>
        <section class="mb-4" style="color:black;">
            <h5>Artículo 19. Funciones del Presidente.</h5>
            <ol>
                <li>El Presidente asumirá la más alta representación de la Asociación y contará con voto
                    de calidad en caso de empate en las decisiones de la Junta Directiva. El mismo podrá
                    ser cualquier socio mayor de edad y en pleno ejercicio de su capacidad de obrar.</li>
                <li>Tendrá las siguientes competencias:
                    <ol style="list-style-type:lower-alpha">
                        <li>Convocar las reuniones de la Asamblea General Ordinaria y Extraordinaria, según el
                            procedimiento establecido al efecto, así como fijar el correspondiente orden del día,
                            con los temas propuestos por la propia Asamblea, la Junta Directiva o cualquier socio,
                            y hacerlo público al momento de la convocatoria de la Asamblea General.</li>
                        <li>Presidir y moderar las reuniones de la Asamblea General y de la Junta Directiva.</li>
                    </ol>
                </li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 20. Funciones del Secretario general.</h5>
            <ol>
                <li>El Secretario General es el miembro de la Junta Directiva encargado de velar por el
                    cumplimiento de las obligaciones documentales y registrales de la Asociación.</li>
                <li>Corresponde al Secretario General:
                    <ol style="list-style-type:lower-alpha">
                        <li>Redactar y custodiar las actas de las sesiones de la Asamblea General y de la Junta
                            Directiva, firmándolas junto con el Presidente.</li>
                        <li>Conservar un Registro de Socios actualizado.</li>
                        <li>Coordinarse con el Presidente en el reparto y ejecución de las tareas administrativas
                            necesarias para el correcto funcionamiento de la Asociación.</li>
                    </ol>
                </li>
                <li>En caso de vacante, ausencia o enfermedad, el Secretario General será sustituido
                    por el Vocal designado por la Junta Directiva hasta la cobertura del cargo.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 21. Funciones del Vicepresidente.</h5>
            <ol>
                <li>Sustituye al Presidente en caso de ausencia, enfermedad, vacante o delegación.</li>
                <li>Colabora con el Presidente en el ejercicio de sus funciones.</li>
                <li>Ejerce aquellas funciones que le sean expresamente encomendadas por la Asamblea
                    General o por la Junta Directiva.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 22. Funciones del Tesorero.</h5>
            <ol>
                <li>El Tesorero tendrá las siguientes competencias:
                    <ol style="list-style-type:lower-alpha">
                        <li>Llevar la contabilidad de la Asociación y custodiar sus documentos económicos y
                            financieros.</li>
                        <li>Supervisar y administrar, en caso de existir, el Tesoro de la Asociación.</li>
                    </ol>
                </li>
                <li>El Tesorero podrá proponer cambios en las aportaciones y patrimonio de la
                    Asociación, lo cual será aprobado en todo caso por la Junta Directiva.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 23. Publicidad de los acuerdos.</h5>
            <p>Los acuerdos adoptados en el seno de la Junta Directiva quedarán fijados en
                Decisiones. Estas deberán contar con la firma tanto del Presidente como del Secretario
                General y ser publicadas en el apartado oficial habilitado en la web de la Asociación.
                Su entrada en vigor tendrá lugar al momento de publicarse.</p>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 24. Incompatibilidades y prohibiciones.</h5>
            <ol>
                <li>Los cargos de Presidente y Secretario General no podrán ser ejercidos por la misma
                    persona.</li>
                <li>Los miembros de la Junta Directiva no podrán percibir retribución alguna por el
                    desempeño de su cargo, sin perjuicio de que se les reembolsen los gastos
                    debidamente justificados.</li>
                <li>Ningún miembro podrá ostentar simultáneamente más de un cargo dentro de la
                    Junta Directiva, exceptuando la posibilidad de que una misma persona ocupe los
                    cargos de Vicepresidente y Secretario General.</li>
                <li>Las incompatibilidades previstas en la legislación vigente se aplicarán en todo caso.</li>
            </ol>
        </section>

        <br>
        <h4 style="font-weight:bold;">CAPÍTULO V. DE LOS ASOCIADOS</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 25. Adquisición de la condición de socio e información requerida.</h5>
            <ol>
                <li>La condición de socio se adquirirá una vez incluido en el Registro de Socios,
                    mediante inscripción por la página web de la Asociación, o por el cauce previsto a tal
                    efecto.</li>
                <li>El Registro de Socios será el documento oficial donde se incluirán los datos de los
                    socios. Se reflejarán en él, como mínimo, los siguientes datos:
                    <ol style="list-style-type:lower-alpha">
                        <li>Nombre y apellidos.</li>
                        <li>Correo electrónico de contacto.</li>
                        <li>Estudios de Grado o Máster matriculados en la Universidad Carlos III de Madrid.</li>
                    </ol>
                </li>
                <li>La Asociación garantizará en todo momento la confidencialidad y seguridad de los
                    datos personales de los socios, conforme al Reglamento (UE) 2016/679 y a la Ley
                    Orgánica 3/2018, de Protección de Datos Personales y garantía de los derechos
                    digitales. Los socios podrán ejercitar sus derechos de acceso, rectificación, supresión,
                    limitación, oposición y portabilidad dirigiéndose a la Junta Directiva a través de los
                    cauces oficiales de comunicación de la Asociación.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 26. Régimen Sancionador.</h5>
            <ol>
                <li>El régimen sancionador regulará las infracciones y sanciones de los socios en
                    relación con el incumplimiento de los Estatutos, del Reglamento de Régimen Interno o
                    de los acuerdos válidamente adoptados por los órganos de gobierno de la Asociación.</li>
                <li>La tipificación de infracciones y sanciones, así como el procedimiento, se
                    desarrollará en el Reglamento de Régimen Interno, que deberá respetar en todo caso
                    lo dispuesto en la Ley y en la normativa universitaria aplicable.</li>
                <li>El procedimiento sancionador garantizará siempre:
                    <ol style="list-style-type:lower-alpha">
                        <li>La apertura de expediente con comunicación escrita al socio afectado.</li>
                        <li>El derecho del socio a ser oído y a presentar alegaciones y pruebas en su defensa.</li>
                        <li>La decisión motivada de la Junta Directiva, que propondrá la sanción
                            correspondiente.</li>
                        <li>El derecho del socio a recurrir la sanción ante la Asamblea General Extraordinaria,
                            que decidirá en última instancia mediante votación secreta.</li>
                    </ol>
                </li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 27. Derechos de los socios.</h5>
            <p>Son derechos de todos los socios:</p>
            <ol style="list-style-type:lower-alpha">
                <li>Participar de hecho, con voz y voto en los órganos de la Asociación.</li>
                <li>Utilizar los servicios de la Asociación, reunirse en sus locales y expresarse a través de
                    sus órganos.</li>
                <li>Defender, en el seno de la Asociación, cualquier planteamiento referente a la
                    actuación, la planificación y el desarrollo de sus actividades.</li>
                <li>Ser electores y elegibles como miembros de la Junta Directiva o, en su caso, de los
                    grupos de trabajo, siempre que cumplan con lo estipulado en estos Estatutos.</li>
                <li>Exigir información de cualquier índole sobre el funcionamiento de la Asociación.</li>
                <li>Salvaguardar su integridad como socios, mediante pruebas documentales y
                    testificales, en caso de discutirse la aplicación de medidas del Régimen Sancionador
                    que les afecten.</li>
                <li>Ser oídos y defenderse ante la Asamblea General Extraordinaria en caso de que la
                    Junta Directiva proponga su expulsión.</li>
                <li>Disfrutar de todos los demás derechos que, como socios, puedan corresponderles
                    conforme a la legislación vigente en materia de Asociaciones, a la normativa
                    universitaria aplicable y al resto del ordenamiento jurídico.</li>
            </ol>
        </section>
        <section class="mb-4" style="color:black;">
            <h5>Artículo 28. Deberes de los socios</h5>
            <p>Serán deberes de todos los socios:</p>
            <ol style="list-style-type:lower-alpha">
                <li>Respetar y cumplir estos Estatutos, así como los acuerdos adoptados por los órganos de la
                    Asociación.</li>
                <li>Fomentar el crecimiento de la Asociación mediante la promoción de sus actividades.</li>
                <li>Desempeñar los cargos para los que sean elegidos.</li>
                <li>Contribuir a la financiación de la Asociación mediante la oportuna cuota, en caso de ser ésta
                    aprobada por la Asamblea General.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 29. Pérdida de condición de socio</h5>
            <p>La condición de socio se perderá por las siguientes causas:</p>
            <ol style="list-style-type:lower-alpha">
                <li>Por voluntad propia, comunicada por escrito a la Junta Directiva</li>
                <li>Por las causas previstas en el Régimen Sancionador de la Asociación, siguiendo en todo caso el
                    procedimiento establecido en el artículo 26.</li>
                <li>Por expulsión, de acuerdo con lo establecido en los artículos 26 y 30 de estos Estatutos.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 30. Expulsión de un socio</h5>
            <ol>
                <li>La expulsión de cualquier socio será aprobada en Asamblea General Extraordinaria a propuesta de la
                    Junta Directiva</li>
                <li>Se seguirá siempre el procedimiento establecido en el artículo 26, garantizando el derecho de
                    audiencia y recurso.</li>
                <li>La votación en la que se apruebe la expulsión de un socio en Asamblea General se hará de manera
                    secreta.</li>
            </ol>
        </section>

        <br>
        <h4 style="font-weight:bold;">CAPÍTULO VI. DE LOS CARGOS DELEGADOS Y RESPONSABLES DE ÁREA</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 31. Naturaleza y finalidad</h5>
            <ol>
                <li>La Asociación podrá contar con cargos delegados o responsables de área, designados entre los socios,
                    para el desempeño de funciones específicas necesarias para el cumplimiento de los fines de la
                    Asociación, tales como gestión web, coordinación de proyectos, comunicación, marketing u otras que
                    determine la Junta Directiva</li>
                <li>Estos cargos tendrán carácter funcional y auxiliar, sin integrar necesariamente la Junta Directiva,
                    salvo que el designado forme ya parte de ella.</li>
                <li>Los cargos delegados actuarán bajo la supervisión de la Junta Directiva y rendirán cuentas de su
                    labor ante la misma.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 32. Nombramiento y duración</h5>
            <ol>
                <li>La designación de los cargos delegados corresponde a la Junta Directiva, que podrá nombrarlos y
                    cesarlos libremente, en atención a las necesidades de la Asociación.</li>
                <li>El nombramiento deberá comunicarse a la Asamblea General en la primera sesión ordinaria que se
                    celebre tras su adopción.</li>
                <li>La duración de los cargos delegados se vincula al mandato de la Junta Directiva que los nombre,
                    cesando automáticamente en caso de renovación de esta, salvo confirmación expresa de la nueva Junta.
                </li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 33. Cese</h5>
            <p>Los cargos delegados cesarán por las siguientes causas:</p>
            <ol style="list-style-type:lower-alpha">
                <li>Expiración del mandato de la Junta Directiva que los designó, salvo confirmación expresa por la
                    nueva Junta.</li>
                <li>Renuncia expresa del interesado, comunicada por escrito a la Junta Directiva.</li>
                <li>Pérdida de la condición de socio o de la capacidad de obrar.</li>
                <li>Acuerdo de la Junta Directiva.</li>
                <li>Acuerdo de la Asamblea General</li>
            </ol>
        </section>
        <br>
        <h4 style="font-weight:bold;">CAPÍTULO VII. DE LOS RECURSOS ECONÓMICOS, FINANCIACIÓN Y CONTABILIDAD DE LA
            ASOCIACIÓN</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 34. Patrimonio inicial.</h5>
            <p>La Asociación nace sin patrimonio fundacional ni aportaciones iniciales obligatorias por parte de los
                socios.</p>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 35. Cierre de Ejercicio.</h5>
            <p>El cierre del ejercicio asociativo se fija en el día 30 de junio.</p>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 36. Métodos de financiación.</h5>
            <ol>
                <li>La Asociación podrá financiarse a través de las siguientes vías:
                    <ol style="list-style-type:lower-alpha">
                        <li>Aportaciones solidarias, voluntarias o extraordinarias de sus socios.</li>
                        <li>Cuotas ordinarias o extraordinarias, que solo podrán establecerse previa aprobación por la
                            Asamblea General, a propuesta de la Junta Directiva y acompañada de un informe motivado del
                            Tesorero que justifique su necesidad.</li>
                        <li>Subvenciones, ayudas públicas o privadas, donaciones, legados o cualquier otra aportación de
                            terceros.</li>
                        <li>Rendimientos procedentes de actividades, servicios o publicaciones propias, siempre dentro
                            de los fines de la Asociación y conforme a la legalidad vigente.</li>
                    </ol>
                </li>
                <li>Mientras no se acuerde lo contrario por la Asamblea General, las aportaciones de los socios tendrán
                    carácter voluntario y no obligatorio.</li>
                <li>La gestión de los recursos económicos corresponderá a la Junta Directiva, bajo la supervisión del
                    Tesorero, quien rendirá cuentas anualmente ante la Asamblea General.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 37. Gastos.</h5>
            <p>Todo gasto propuesto por la Junta Directiva se someterá al examen del Tesorero, quien presentará un
                informe en el que dará su dictamen al respecto. La Junta Directiva, una vez oído el informe, será la
                encargada de tomar la decisión correspondiente.</p>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 38. Obligaciones documentales y contables.</h5>
            <ol>
                <li>La Asociación dispondrá de una relación actualizada de asociados. Además, llevará una contabilidad
                    donde se reflejará la fiel imagen del patrimonio, del resultado y de su situación financiera, así
                    como registrará las actividades realizadas y efectuará un inventario de sus bienes.</li>
                <li>Se recogerá en un libro de actas la información sobre las reuniones de los órganos de gobierno de la
                    Asociación.</li>
                <li>Toda la información descrita en este artículo deberá ser pública y accesible para todos los
                    asociados, siendo publicada preferiblemente en la web oficial de la Asociación.</li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 39. Reforma estatutaria.</h5>
            <p>Los Estatutos podrán ser modificados en la Asamblea General Extraordinaria, mediante acuerdo por mayoría
                absoluta de los socios activos presentes.</p>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 40. Principios básicos de la reforma estatutaria.</h5>
            <ol>
                <li>No se podrá eliminar en una reforma de estos Estatutos ningún precepto esencial para el correcto
                    funcionamiento de la Asociación, ni incluir artículos contrarios a la normativa vigente y al resto
                    del ordenamiento jurídico.</li>
                <li>Cualquier futura reforma estatutaria mantendrá la integridad, principios básicos y coherencia de
                    estos Estatutos, tratando siempre de favorecer el cumplimiento de los fines para los que esta
                    asociación ha sido creada.</li>
            </ol>
        </section>


        <br>
        <h4 style="font-weight:bold;">CAPÍTULO VIII. DE LA DISOLUCIÓN DE LA ASOCIACIÓN</h4>
        <hr style="border: none; border-top: 1px solid #a5a5a5; margin: 12px 0;">
        <section class="mb-4" style="color:black;">
            <h5>Artículo 41. Causas de disolución</h5>
            <ol>
                <li>La Asociación se disolverá por los siguientes motivos:
                    <ol style="list-style-type:lower-alpha">
                        <li>Por voluntad de sus socios activos expresada mediante la aprobación por mayoría
                            absoluta en una Asamblea General Extraordinaria, como recogen estos Estatutos.</li>
                        <li>Por sentencia judicial</li>
                        <li>Por las demás causas dispuestas en la normativa vigente y que resulte de aplicación.</li>
                    </ol>
                </li>
            </ol>
        </section>

        <section class="mb-4" style="color:black;">
            <h5>Artículo 42. Disposición del patrimonio en caso de disolución</h5>
            <p>En caso de liquidación, el patrimonio de la Asociación se destinará a una institución de
                la Universidad Carlos III de Madrid o, en su defecto, a una entidad sin ánimo de lucro
                con fines similares, conforme a lo dispuesto en la Ley 1/2002. El beneficiario será
                aprobado en la Asamblea General Extraordinaria a propuesta de la Junta Directiva y
                deberá contar con el apoyo de la mayoría simple de los socios activos.</p>
        </section>
